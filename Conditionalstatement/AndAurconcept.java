package Conditionalstatement;

public class AndAurconcept {

	public static void main(String[] args) {
		
		int a=5;
		int b=6;
		int c=4;		
		
		System.out.println((a>b)&&(a>c));
		System.out.println((b>a)&&(b>c));
		System.out.println((c>a)&&(c>b));
		
		

		System.out.println((a>b)||(a>c));
		System.out.println((b>a)||(b>c));
		System.out.println((c>a)||(c>b));

	
	}
	
	
	
	
	
	
}
